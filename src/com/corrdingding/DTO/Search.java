package com.corrdingding.DTO;

public class Search {
	private String search_code;
	private String user_id;
	private String search_word;
	private String search_reg_time;
	public String getSearch_code() {
		return search_code;
	}
	public void setSearch_code(String search_code) {
		this.search_code = search_code;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getSearch_word() {
		return search_word;
	}
	public void setSearch_word(String search_word) {
		this.search_word = search_word;
	}
	public String getSearch_reg_time() {
		return search_reg_time;
	}
	public void setSearch_reg_time(String search_reg_time) {
		this.search_reg_time = search_reg_time;
	}
	

}
